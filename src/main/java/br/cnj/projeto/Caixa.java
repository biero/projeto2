package br.cnj.projeto;

public class Caixa {
	private Integer id_caixa;
	private String ds_caixa;
	private Integer in_sistema;
	
	public Caixa() {
	}

	public Integer getId_caixa() {
		return id_caixa;
	}

	public void setId_caixa(Integer id_caixa) {
		this.id_caixa = id_caixa;
	}

	public String getDs_caixa() {
		return ds_caixa;
	}

	public void setDs_caixa(String ds_caixa) {
		this.ds_caixa = ds_caixa;
	}

	public Integer getIn_sistema() {
		return in_sistema;
	}

	public void setIn_sistema(Integer in_sistema) {
		this.in_sistema = in_sistema;
	}
}
